-- Curriculum Management System Schema
-- Targeted DB: MySQL 8.0+

CREATE DATABASE IF NOT EXISTS applet_db;
USE applet_db;

-- 1. Weeks Table
CREATE TABLE weeks (
    week_id INT PRIMARY KEY,
    title VARCHAR(255) NOT NULL,
    description TEXT,
    milestone TEXT,
    status ENUM('green', 'yellow', 'red') DEFAULT 'red',
    is_locked BOOLEAN DEFAULT TRUE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 2. Key Knowledge Table
CREATE TABLE key_knowledge (
    id INT AUTO_INCREMENT PRIMARY KEY,
    week_id INT,
    knowledge_text TEXT NOT NULL,
    difficulty INT,
    target_day INT,
    FOREIGN KEY (week_id) REFERENCES weeks(week_id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 3. Daily Tasks Table
CREATE TABLE tasks (
    task_id VARCHAR(50) PRIMARY KEY,
    week_id INT,
    day_number INT,
    task_type ENUM('warmup', 'new_knowledge', 'practice', 'review') NOT NULL,
    title VARCHAR(255) NOT NULL,
    duration VARCHAR(50),
    content TEXT,
    is_completed BOOLEAN DEFAULT FALSE,
    FOREIGN KEY (week_id) REFERENCES weeks(week_id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 4. Questions Table
CREATE TABLE questions (
    question_id VARCHAR(50) PRIMARY KEY,
    task_id VARCHAR(50),
    question_type VARCHAR(50),
    question_body TEXT NOT NULL,
    answer TEXT NOT NULL,
    options_json JSON,
    explanation TEXT,
    FOREIGN KEY (task_id) REFERENCES tasks(task_id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

